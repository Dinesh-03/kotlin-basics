fun main(){

    // String variables are used to hold multiple characters strung together
    // You create a String variable by assigning the characters enclosed in double quotes

    var name = "Avinash"

    println(name) // Avinash
    println(name::class) // class java.lang.String

    // explicit type declaration
    var passion: String
    passion = "Programming"

    println(passion) // Programming
    println(passion::class) // class java.lang.String

}