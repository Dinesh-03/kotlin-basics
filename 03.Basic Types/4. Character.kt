fun main(){

    // 'Char' variables are used for single characters
    // You create a Char variable by assigning a character in single quotes

    var gender = 'M'

    println(gender) // M
    println(gender::class) // char

    // explicit type declaration
    var alphabet: Char
    alphabet = 'x'

    println(alphabet) // x
    println(alphabet::class) // char

}