fun main(){

    // Boolean variables are used for values that can either be 'true' or 'false'

    var isKotlinPopular = true
    println(isKotlinPopular::class) // boolean

    var isHTMLProgrammingLanguage = false
    println(isHTMLProgrammingLanguage::class) // boolean

    // explicit type declaration
    var isDead: Boolean
    isDead = false
    println(isDead::class) // // boolean

}